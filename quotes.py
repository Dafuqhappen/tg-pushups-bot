def _pick_unused(bucket: str, pool: list[str]) -> str:
    used = db.get_used_quotes(bucket)
    unused = [q for q in pool if q not in used]
    if not unused:
        db.clear_used_quotes(bucket)
        unused = list(pool)
    quote = random.choice(unused)
    db.mark_quote_used(bucket, quote)
    return quote


def random_motivational() -> str:
    return _pick_unused("motivational", MOTIVATIONAL)


def random_bros() -> str:
    return _pick_unused("bros", BROS)
